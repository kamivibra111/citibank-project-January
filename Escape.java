public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello \n*\n* *\n* * *\n* * * *\n* * * * *");
        System.out.println();
        System.out.println("* * * * *\n* * * *\n* * *\n* *\n*");
        System.out.println("        *\n      * *\n    * * *\n  * * * *\n* * * * *");
        System.out.println("Muhammad\tKamran\tKhan");
        System.out.println("Escape the e\b"); // "e" is removed
        System.out.println("\\Kamran\\");
        System.out.println("\"kamran\" ");

    }
}
